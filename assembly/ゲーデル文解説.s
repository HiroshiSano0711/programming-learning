「数」そのものは代入できない
「数を表す記号列（数詞）」だけが代入可能


１：内部で起きているのはこれだけ

G := Θ(⌜Θ⌝)

「ある数を、ある式の変数に代入した」

それ以上のことは何もしていない。


２：自己言及は「外部の読み」で初めて現れる

これが決定的に重要。

内部（構文論）

Θ(⌜Θ⌝) はただの式であって、「自己」「私」「言及」など一切ない

真理・意味・参照は使っていない

外部（意味論・評価主体）

⌜Θ⌝ は「Θ を表す数」

だからこの式は「Θ が自分自身に適用されている」と読めてしまう

自己言及として読めるは“発見される”のであって、内部で定義されていない



３：任意の式 φ(x) に対して、φ(⌜φ⌝) という文が作れる

これが 対角化補題。

φ は「1変数の式」

⌜φ⌝ はその式のゲーデル数

φ(⌜φ⌝) は完全に合法な算術文

ここまで一切、意味の話をしていない。



