// Listing 16-1
//
// "//" 以降はその行末までコメントとして扱われる。
// ".text" ディレクティブは、この後に続く命令列を
// 実行用コード領域（テキストセクション）に配置する
// 指示である。

.text

// ここに「main」関数相当の処理を書く。
// （この例では C/C++ の main に依存せず、
//  アセンブリのみで完結するスタンドアロン
//  プログラムとして動作することを前提にしている）

// macOS では main のシンボル名は先頭にアンダースコアを付け
// 「_main」としなければならない。
// 一方、Linux では通常アンダースコアは不要（"main"で可）。

// ".global _main" は、_main というシンボルを
// 他のモジュール（リンカ）から参照可能にする宣言。
// 実行ファイル生成時にエントリポイントとして
// 解決されるため、グローバル公開が必要。

.global _main
.global main

// ".align 2" は、次に続くコードを 4 バイト境界に配置する指示。
// ARM CPU は命令フェッチ効率のため、基本的に 4 バイト境界での
// アラインメントが必要となる。
// "2" は「2 の 2乗 = 4」を意味し、4バイト境界を指定している。

.align 2

// 以下が本体ルーチン。
// 内容は ret 1命令だけで、実質的に何もせず OS に制御を返す。
// （戻り値は特に設定していないため、終了コードは未定義）

_main:
main:
       ret
