# 対角化の直感的な説明を再掲
# 関数f(x) =「x 番目の関数」が与えられたとき、g(x) = f(x)(x) を対角線上の値において、
# 全て逆にすることで 関数f の列挙に含まれない 関数g を作る
#
####################
##### 一例として ######
####################
# f : X → (Y → Z)
# これは集合論的に定義された写像の写像
#
# f : ℕ → (ℕ → {0,1})
# ここで {0,1} は真理値ではなく単なる二値集合
#
# f(0), f(1), f(2), ... は写像の列
# f(x) は 列のx番目の写像
#
# f(x)(y) は 列のx番目の写像にyを入力したときの値。
#
# f(x)(x)  対角線上：x = y
#
# f(0)(0)
# f(1)(1)
# f(2)(2)
# f(3)(3)
# f(4)(4)
# ...
#
# 対角線上の全点を否定 → 対角線上の値が逆になる → f の列挙に含まれない gが作れる
#   g(x) := NOT( f(x)(x) )
# ※NOT は論理否定ではなく、それぞれの点で補集合をとる
#
# f(x)(x)は（直感的な意味での）自己言及・対角線・自己適用が一目で分かる便利な表記
#
# ゲーデルの場合は式 + 数詞 + 代入しか使っていない
#   Proof(p, x) は二変数の証明関係
#   Prov(x) := ∃p Proof(p, x)
#
# ゲーデル文は
#  「自己を参照する文について、その証明の存在を否定する」
# という形で対角線構造を実現している
