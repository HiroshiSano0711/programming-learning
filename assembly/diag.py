#
# 証明関係（2変数）→畳込みで1つに
def Proof(p, x):
    # p は「証明のコード」
    # x は「文のコード」
    return f"Proof({p}, {x})"


# 存在量化による定義
def Prov(x):
    # Prov(x) ≡ ∃p Proof(p, x)
    # 'p' は 束縛変数名としての p
    return f"∃p {Proof('p', x)}"


# ¬Prov を明示的に作る
def Not(expr):
    return f"¬({expr})"


############################
########## 外部 #############
############################
#
# Φ(⌜G⌝) : 一変数をとる式
# x には "数詞"が入ってくる
def codeG(x):
    return (
        f"{x} は、"
        f"{Not(Prov(f'⌜{x}⌝'))} "
        "という形の文である。"
    )

# ゲーデル数化の代用品の関数
def code_of(G):
    # G（文を表す記号列）を、それを一意に表すデータに変換する
    # ゲーデル数化の超簡略版（構文のみを対象）
    return repr(G)


# 対角化を行う関数の簡略版
# 注意: 本来の ⌜φ⌝ は「式のゲーデル数」
# ここでは Python 関数オブジェクトを、そのまま repr してコードの代用品にしている
# （構造理解のための近似）
def diagonalize(expr_func):
    # φ ↦ φ(⌜φ⌝)
    return lambda: expr_func(code_of(expr_func))


# --- 構成（対角化補題の実演） ---
# メタ理論（外部）で行う構成操作
# その評価結果として得られる文字列（＝文）Gが形式体系 T の内部で扱われる文
G = diagonalize(codeG)()

# --- 数学的定義（最終的に扱う対象） ---
# G ≡ ¬Prov(⌜G⌝)
#
# (外部)
# φ(x) を取る
# ↓
# 対角化補題で G を構成
# ↓
# (内部)
# G ≡ ¬Prov(⌜G⌝)
#
#####################################
########## 内部で扱われる文 #############
#####################################
#
# 操作としての構成は外部（メタ理論）
# 結果としての文 G は内部で定義可能・言及可能
print(G)

